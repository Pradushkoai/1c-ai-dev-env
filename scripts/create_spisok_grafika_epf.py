        ДобавитьОписаниеКолонки(Колонки, "УведомленияСформированы", "Уведомления сформированы", 12);
        ДобавитьОписаниеКолонки(Колонки, "ВсеМеткиОтсканированы", "Все метки отсканированы", 12);
