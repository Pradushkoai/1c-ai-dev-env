#!/usr/bin/env python3
"""
Создание обработки "СписокГрафикаИВыполненияОбхода" —
форма списка документов ГрафикИВыполнениеОбхода из конфигурации «Обход».

Подход:
    1. Берём оригинальный EPF как шаблон (валидируется v8unpack round-trip).
    2. Меняем имя, синоним, UUID обработки.
    3. BSL-модуль формы полностью переписываем: при создании формы программно
       создаётся реквизит «Список» типа ДинамическийСписок и таблица с колонками
       документа ГрафикИВыполнениеОбхода. Это валидный паттерн для внешних
       обработок, когда форма нужна простой и не требует ручной верстки в Конфигураторе.
"""
from __future__ import annotations

import shutil
import sys
import uuid
from pathlib import Path

# Добавляем scripts в path
sys.path.insert(0, str(Path(__file__).parent.parent / "scripts"))

from epf_builder import (
    extract_epf,
    build_epf,
    rename_data_processor,
    replace_form_module,
)

# ─── Параметры обработки ─────────────────────────────────────────
TEMPLATE_EPF = Path("/home/z/my-project/upload/ОбходТерриторииСПереключателемФонСоЗвуком.epf")
OUTPUT_EPF = Path("/home/z/my-project/download/СписокГрафикаИВыполненияОбхода.epf")

PROC_NAME = "СписокГрафикаИВыполненияОбхода"
PROC_SYNONYM = "Список графика и выполнения обхода"
FORM_NAME = "Форма"

# Основная таблица динамического списка
MAIN_TABLE = "Документ.ГрафикИВыполнениеОбхода"

# Реквизиты шапки документа для отображения в списке
# (код_в_1С, отображаемое_имя, ширина)
COLUMNS = [
    ("Date",                       "Дата",                          12),
    ("Number",                     "Номер",                         12),
    ("Posted",                     "Проведен",                       6),
    ("ОтветственныйЗаОбход",       "Ответственный за обход",        20),
    ("ВремяСтарта",                "Время старта",                  16),
    ("ВремяОкончания",             "Время окончания",               16),
    ("УведомленияСформированы",    "Уведомления сформированы",      12),
    ("ВсеМеткиОтсканированы",      "Все метки отсканированы",       12),
    ("ОтправленоНаСервер",         "Отправлено на сервер",          10),
]


# ─── BSL-модуль формы ────────────────────────────────────────────
# ВАЖНО: Конфигурация «Обход» — мобильное приложение (MobilePlatformApplication).
# В мобильном приложении НЕ поддерживается:
#   - Тип ПараметрыФормыДинамическогоСписка (это серверный тип толстого клиента)
#   - Программное создание ДинамическийСписок через Новый РеквизитФормы(...) с параметрами
#   - Установка ОсновнаяТаблица у DynamicList в рантайме
# ДинамическийСписок в мобильном приложении можно использовать ТОЛЬКО статически,
# объявив его в форме через Конфигуратор (XML метаданных формы).
#
# Поэтому внешняя обработка реализована как ОБЁРТКА:
#   - При открытии формы обработки сразу открывается общая форма «ФормаОбходов»
#     из конфигурации «Обход» (там таблица ТаблицаОбходов с документами
#     ГрафикИВыполнениеОбхода — у самого документа своих форм нет)
#   - Сама форма обработки закрывается (Отказ = Истина)
#   - Никаких мобильных ограничений не нарушается
BSL_MODULE = """\
// СписокГрафикаИВыполненияОбхода - модуль формы
// Внешняя обработка-обертка: открывает общую форму «ФормаОбходов»
// из конфигурации «Обход» (мобильное приложение), где размещен список
// документов ГрафикИВыполнениеОбхода в таблице ТаблицаОбходов.
//
// В мобильном приложении тип ПараметрыФормыДинамическогоСписка недоступен,
// а у документа ГрафикИВыполнениеОбхода нет собственных форм - поэтому
// используется готовая общая форма списка из конфигурации.

#Область ПрограммныйИнтерфейс

// Возвращает имя общей формы «ФормаОбходов» из конфигурации «Обход».
// Эта форма содержит таблицу ТаблицаОбходов со списком документов
// ГрафикИВыполнениеОбхода (у самого документа отдельных форм нет).
//
// Возвращаемое значение:
//  Строка - полное имя общей формы в формате "ОбщаяФорма.<Имя>".
&НаСервере
Функция ПолучитьИмяФормыСписка() Экспорт
        Возврат "ОбщаяФорма.ФормаОбходов";
КонецФункции

#КонецОбласти

#Область СлужебныеПроцедурыИФункции

// Открывает стандартную форму списка документа ГрафикИВыполнениеОбхода.
// Вынесена в отдельную процедуру для удобства переиспользования и тестирования.
&НаКлиенте
Процедура ОткрытьФормуСпискаОбходов()
        ОткрытьФорму(ПолучитьИмяФормыСписка());
КонецПроцедуры

#КонецОбласти

#Область ОбработчикиСобытийФормы

// При открытии формы обработки - сразу открываем стандартную форму списка
// документа ГрафикИВыполнениеОбхода и отменяем открытие текущей формы.
// Таким образом пользователь видит сразу список, без промежуточного экрана.
&НаКлиенте
Процедура ПриОткрытии(Отказ)
        ОткрытьФормуСпискаОбходов();
        Отказ = Истина;
КонецПроцедуры

#КонецОбласти
"""


# ─── Сборка ─────────────────────────────────────────────────────
def main():
    print(f"=== Создание обработки {PROC_NAME} ===")
    print(f"Шаблон:     {TEMPLATE_EPF}")
    print(f"Результат:  {OUTPUT_EPF}")
    print(f"Форма:      список документов {MAIN_TABLE}")
    print()

    if not TEMPLATE_EPF.exists():
        print(f"❌ Шаблон не найден: {TEMPLATE_EPF}")
        sys.exit(1)

    work_dir = Path(f"/tmp/epf_{PROC_UUID_PREFIX}")
    if work_dir.exists():
        shutil.rmtree(work_dir)
    src_dir = work_dir / "src"

    # 1. Распаковка шаблона
    print("1. Распаковка шаблона...")
    res = extract_epf(TEMPLATE_EPF, src_dir)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    print(f"   Распаковано файлов: {len(res['files'])}")

    # 2. Переименование обработки (имя, UUID, синоним)
    print(f"2. Переименование обработки → {PROC_NAME}...")
    res = rename_data_processor(src_dir, PROC_NAME, PROC_SYNONYM)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    print(f"   Старое имя: {res['old_name']}")
    print(f"   Новое имя:  {res['new_name']}")
    print(f"   Новый UUID: {res['new_uuid']}")

    # 3. Замена BSL-модуля формы
    print("3. Замена BSL-модуля формы...")
    res = replace_form_module(src_dir, FORM_NAME, BSL_MODULE)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    print(f"   Модуль формы: {res['lines']} строк")

    # 4. Сборка .epf
    print("4. Сборка .epf...")
    OUTPUT_EPF.parent.mkdir(parents=True, exist_ok=True)
    res = build_epf(src_dir, OUTPUT_EPF)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    print(f"   Размер: {res['size_bytes']} байт ({res['size_bytes']/1024:.1f} КБ)")
    print(f"   Файлов в исходниках: {res['files_included']}")

    # 5. Проверка round-trip
    print("5. Проверка round-trip...")
    from epf_builder import verify_epf
    res = verify_epf(OUTPUT_EPF)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    print(f"   Сигнатура: {res['signature']} (валидная)")
    print(f"   Извлечено файлов: {res['files_extracted']}")

    # 6. Проверяем имя в собранном файле
    print("6. Проверка имени обработки в собранном файле...")
    import json as _json
    check_dir = work_dir / "check"
    if check_dir.exists():
        shutil.rmtree(check_dir)
    res = extract_epf(OUTPUT_EPF, check_dir)
    if not res["ok"]:
        print(f"❌ {res['error']}")
        sys.exit(1)
    with open(check_dir / "ExternalDataProcessor.json", "r", encoding="utf-8") as f:
        meta = _json.load(f)
    print(f"   name:    {meta['name']}")
    print(f"   uuid:    {meta['uuid']}")
    print(f"   synonym: {meta.get('name2', {}).get('ru')}")

    # Чистим temp
    try:
        shutil.rmtree(work_dir)
    except Exception:
        pass

    print()
    print(f"✅ EPF файл создан: {OUTPUT_EPF}")
    print(f"   Размер: {OUTPUT_EPF.stat().st_size / 1024:.1f} КБ")
    return OUTPUT_EPF


# Глобальная константа для имени temp-каталога
PROC_UUID_PREFIX = "sp_gvo"


if __name__ == "__main__":
    main()
