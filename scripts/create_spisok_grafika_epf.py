                ОписаниеКолонки = СоставКолонок[Индекс];
                ИмяЭлемента = "Список" + ОписаниеКолонки.Поле;

                Колонка = Элементы.Добавить(ИмяЭлемента, Тип("ПолеФормы"), ТаблицаСписка);
                Колонка.ПутьКДанным = "Список." + ОписаниеКолонки.Поле;
                Колонка.Вид = ВидПоляФормы.ПолеВвода;
