@register_template(
    name="json_read_file",
    description="Чтение JSON файла",
    category="json",
    parameters=[],
)
def json_read_file() -> str:
    """Прочитать JSON из файла."""
    return """// Чтение JSON файла
Функция ПрочитатьJSON(Знач ПутьКФайлу) Экспорт

    Если Не ФайлСуществует(ПутьКФайлу) Тогда
        ВызватьИсключение "Файл не существует: " + ПутьКФайлу;
    КонецЕсли;

    ТекстJSON = "";
    Текст = Новый ТекстовыйДокумент;
    Текст.Прочитать(ПутьКФайлу);
    ТекстJSON = Текст.ПолучитьТекст();

    ЧтениеJSON = Новый ЧтениеJSON;
    ЧтениеJSON.УстановитьСтроку(ТекстJSON);

    Попытка
        Результат = ПрочитатьJSON(ЧтениеJSON);
        Возврат Результат;
    Исключение
        ВызватьИсключение "Ошибка парсинга JSON: " + ОписаниеОшибки();
    КонецПопытки;

КонецФункции
"""


@register_template(
    name="json_write_file",
    description="Запись JSON файла",
    category="json",
    parameters=[],
)
def json_write_file() -> str:
    """Записать данные в JSON файл."""
    return """// Запись JSON файла
Процедура ЗаписатьJSON(Знач Данные, Знач ПутьКФайлу) Экспорт

    ЗаписьJSON = Новый ЗаписьJSON;
    ЗаписьJSON.УстановитьСтроку();

    // Настройки сериализации
    ПараметрыЗаписи = Новый ПараметрыЗаписиJSON(ПереносСтрокJSON.Авто);
    ЗаписьJSON.Параметры = ПараметрыЗаписи;

    Попытка
        ЗаписатьJSON(ЗаписьJSON, Данные);
        СтрокаJSON = ЗаписьJSON.Закрыть();

        Текст = Новый ТекстовыйДокумент;
        Текст.УстановитьТекст(СтрокаJSON);
        Текст.Записать(ПутьКФайлу, КодировкаТекста.UTF8);

    Исключение
        ВызватьИсключение "Ошибка записи JSON: " + ОписаниеОшибки();
    КонецПопытки;

КонецПроцедуры
"""


