# ============================================================================

@dataclass
class TemplateInfo:
    """Метаданные шаблона."""

    name: str
    description: str
    category: str            # catalog, document, query, form, http, json, security, etc.
    parameters: list[str]   # имена параметров
    generator: Callable[..., str]


_TEMPLATES: dict[str, TemplateInfo] = {}


def register_template(
    name: str,
    description: str,
    category: str,
    parameters: list[str],
) -> Callable[[Callable[..., str]], Callable[..., str]]:
    """Декоратор для регистрации шаблона."""
    def decorator(func: Callable[..., str]) -> Callable[..., str]:
        _TEMPLATES[name] = TemplateInfo(
            name=name,
            description=description,
            category=category,
            parameters=parameters,
            generator=func,
        )
        return func
    return decorator


def get_template(name: str, **kwargs: Any) -> str:
    """Получить BSL-код шаблона по имени.

    Args:
        name: Имя шаблона (см. BSLTemplates.list_templates()).
        **kwargs: Параметры шаблона.

    Returns:
        BSL-код как строка.

    Raises:
        KeyError: Если шаблон не найден.
        TypeError: Если обязательный параметр отсутствует.
    """
    if name not in _TEMPLATES:
        available = ", ".join(sorted(_TEMPLATES.keys()))
        raise KeyError(f"Template '{name}' not found. Available: {available}")
    return _TEMPLATES[name].generator(**kwargs)


def list_templates(category: str | None = None) -> list[TemplateInfo]:
    """Список всех доступных шаблонов.

    Args:
        category: Фильтр по категории (None — все).

    Returns:
        Список TemplateInfo.
    """
    if category is None:
        return list(_TEMPLATES.values())
    return [t for t in _TEMPLATES.values() if t.category == category]


