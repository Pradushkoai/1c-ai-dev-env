@register_template(
    name="background_job_with_wait",
    description="Фоновое задание с ожиданием завершения",
    category="background",
    parameters=["procedure_name"],
)
def background_job_with_wait(procedure_name: str = "МояПроцедура") -> str:
    """Запустить фоновое задание и дождаться завершения."""
    return f"""// Фоновое задание с ожиданием завершения
Функция ЗапуститьФоновоеЗадание(Знач Параметры) Экспорт

    МассивПараметров = Новый Массив;
    МассивПараметров.Добавить(Параметры);

    ФоновоеЗадание = ФоновыеЗадания.Выполнить(
        "{procedure_name}",
        МассивПараметров
    );

    // Ожидание завершения (timeout 60 секунд)
    Таймаут = 60;
    ПрошлоВремени = 0;

    Пока ФоновоеЗадание.Состояние = СостояниеФоновогоЗадания.Активно
        И ПрошлоВремени < Таймаут Цикл

        Приостановить(1000);  // 1 секунда
        ПрошлоВремени = ПрошлоВремени + 1;

    КонецЦикла;

    Если ФоновоеЗадание.Состояние = СостояниеФоновогоЗадания.Завершено Тогда
        Возврат ФоновоеЗадание.ПолучитьРезультат();
    Иначе
        ВызватьИсключение "Фоновое задание не завершилось за " + Таймаут + " сек";
    КонецЕсли;

КонецФункции
"""


