    output_to_spreadsheet_document = staticmethod(output_to_spreadsheet_document)

    form_open_choice_with_handler = staticmethod(form_open_choice_with_handler)
    form_get_template_and_fill = staticmethod(form_get_template_and_fill)

    http_get_request_safe = staticmethod(http_get_request_safe)

    json_read_file = staticmethod(json_read_file)
    json_write_file = staticmethod(json_write_file)

    check_access_rights = staticmethod(check_access_rights)
    transaction_with_rollback = staticmethod(transaction_with_rollback)
    log_to_event_log = staticmethod(log_to_event_log)

    background_job_with_wait = staticmethod(background_job_with_wait)
    background_job_async = staticmethod(background_job_async)

    @staticmethod
    def list_all() -> list[TemplateInfo]:
        """Список всех шаблонов."""
        return list_templates()

    @staticmethod
    def list_by_category(category: str) -> list[TemplateInfo]:
        """Список шаблонов по категории."""
        return list_templates(category)

    @staticmethod
    def get(name: str, **kwargs: Any) -> str:
        """Получить шаблон по имени."""
        return get_template(name, **kwargs)
